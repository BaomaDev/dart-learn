void main() {
  // Switch case statement
  String grade = 'A';
  
  switch(grade) {
    case 'A':
      print('Excellent!');
      break;
    case 'B':
      print('Good Job!');
      break;
    case 'C':
      print('Not Bad!');
      break;
    case 'D':
      print('Bad!');
      break;
    default:
      print('Invalid Grade');
  }
}