void main() {
  // Do while loop
  print("===== Do while loop =====");
  /// Do while loop digunakan untuk melakukan perulangan minimal 1 kali
  /// Do while loop biasanya digunakan ketika tidak diketahui berapa kali perulangan akan dilakukan
  /// Do while loop memiliki 1 bagian, yaitu kondisi
  /// Kondisi: Menentukan kondisi perulangan
  int i = 0;
  do {
    print(i);
    i++;
  } while(i < 5);
}